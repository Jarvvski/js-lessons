///////////
// Loops //
///////////

// for loop
for (var counter = 0; counter < 10; counter++) {
    console.log(counter);
}

console.log("-----");

// while loop
var counter = 0;
while(counter < 10) {
    console.log(counter++);
}

console.log("-----");

// do while loop
var counter = 0;
do {
    console.log(counter++);
} while(counter < 10);
